--Check if hero have the current itemToBuy in main inventory
function PItems.IsItemInHero(item_name)
	if item_name == 'item_infused_raindrop' then 
		return false; 
	else 
		local bot = GetBot();
		item_name = PItems.NormItemName(item_name);
		local slot = bot:FindItemSlot(item_name);
		local slotType = bot:GetItemSlotType(slot);
		return slotType == ITEM_SLOT_TYPE_MAIN or slotType == ITEM_SLOT_TYPE_BACKPACK;	
	end
end

--Get basic item recursively from ItemUtility data
function PItems.GetBasicItems( ... )
    local basicItemTable = {}
    for i,v in pairs(...) do
        if PItems[v] ~= nil and PItems.IsItemInHero(v) == false then
            for _,w in pairs(PItems.GetBasicItems(PItems[v])) do
				basicItemTable[#basicItemTable+1] = w;
            end
        elseif PItems[v] == nil and PItems.IsItemInHero(v) == false then
			basicItemTable[#basicItemTable+1] = v;
        end
    end
    return basicItemTable
end

function PItems.GetBasicItemsNew( ... )
    local basicItemTable = {}
    for i,v in pairs(...) do
		--print(tostring(v))
		local comps = GetItemComponents(v);
		--print(tostring(#comps))
        if #comps > 0 and PItems.IsItemInHero(v) == false then
            for _,w in pairs(PItems.GetBasicItems(comps[1])) do
				basicItemTable[#basicItemTable+1] = w;
            end
        elseif #comps == 0 and PItems.IsItemInHero(v) == false then
			basicItemTable[#basicItemTable+1] = v;
        end
    end
    return basicItemTable
end

function PItems.GetEmptyInventoryAmount(bot)
	local amount = 0;
	for i=0,8 do	
		local item = bot:GetItemInSlot(i);
		if item == nil then
			amount = amount + 1;
		end
	end
	return amount;
end

function PItems.RemoveItem(tTable, item_name)
	local temp = {};
	for i=1, #tTable do
		if tTable[i] ~= item_name then
			temp[#temp+1] = tTable[i];
		end
	end
	return temp;
end

function PItems.GetItemCharges(bot, item_name)
	local charges = 0;
	for i = 0, 15 do
		local item = bot:GetItemInSlot(i);
		if item ~= nil and item:GetName() == item_name then
			charges = charges + item:GetCurrentCharges();
		end
	end
	return charges;
end

