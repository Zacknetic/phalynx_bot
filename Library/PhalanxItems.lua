function PItems.GetLowestNeutralItemInBackpackIfExcess(bot)
	local lowest_lvl = 6;
	local lowest = nil;
	local num_n_item = 0;
	for i=6,8 do	
		local item = bot:GetItemInSlot(i);
		if item ~= nil 
		then
			local curr_lvl = PItems.GetNeutralItemTier(item:GetName());
			if curr_lvl > 0 then num_n_item = num_n_item + 1 end
			if curr_lvl > 0 and curr_lvl < lowest_lvl then
				lowest_lvl = curr_lvl;
				lowest = item;
			end
		end
	end
	return num_n_item, lowest;
end

function PItems.CanDropExcessNeutralItem(bot)
	local neutral_item = bot:GetItemInSlot(16);
	if neutral_item ~= nil then
		if PItems.GetEmptySlotAmount(bot, ITEM_SLOT_TYPE_BACKPACK) == 0  
		   and PItems.GetEmptySlotAmount(bot, ITEM_SLOT_TYPE_MAIN) == 0  
		then
			local num, lowest = PItems.GetLowestNeutralItemInBackpackIfExcess(bot)
			if num >= 2 then
				return true, lowest;
			end
		end
	end
	return false, nil;
end

function PItems.GetEmptySlotAmount(bot, nType)
	local startIdx = 0;
	local endIdx = 16;
	if nType == ITEM_SLOT_TYPE_MAIN then
		startIdx = 0;
		endIdx = 5;
	elseif nType == ITEM_SLOT_TYPE_BACKPACK then
		startIdx = 6;
		endIdx = 8;
	elseif nType == ITEM_SLOT_TYPE_STASH then	
		startIdx = 9;
		endIdx = 14;
	end
	local amount = 0;
	for i=startIdx,endIdx do	
		local item = bot:GetItemInSlot(i);
		if item == nil then
			amount = amount + 1;
		end
	end
	return amount;
end

