function PItems.CanSwapNeutralItem(bot)
	local neutral_item_slot = 16;
	local active_neutral_item = bot:GetItemInSlot(neutral_item_slot);
	if active_neutral_item ~= nil then
		local ani_lvl = PItems.GetNeutralItemTier(active_neutral_item:GetName());
		local nani_lvl, sni_slot = PItems.GetHighestTierNeutralItem(bot);
		if sni_slot ~= -1 and ani_lvl < nani_lvl then
			return true, neutral_item_slot, sni_slot; 
		end
	else
		local nani_lvl, sni_slot = PItems.GetHighestTierNeutralItem(bot);
		if sni_slot ~= -1 then
			return true, neutral_item_slot, sni_slot; 
		end
	end
	return false, -1, -1;
end

